package org.springframework.social.showcase.mlsListing.model;

public class Tags {
	private String tag_id;
	private String name;
	
	public String getTag_id() {
		return tag_id;
	}
	public void setTag_id(String tag_id) {
		this.tag_id = tag_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
